        <h3 class="title-uppercase">What is NCC?</h3>
        <p>National Cadet Corps is a Tri-Services Organisation comprising the Army, Navy and Air Force, engaged in grooming the youth –‘The Leaders of Tomorrow’ – into disciplined and patriotic citizens. The genesis of the NCC can be traced back to the First World War when the Britishers created the University Corps as the second line of defence and to have a large pool of trained youth available for employment into the Armed Forces.</p>

        <p>After independence on 16 April 1948 the NCC came under the Ministry of Defence, Government of India. </p>
        
        <p>he Girls Division of the NCC was started in July 1949 and in April 1950 Air Wing was added. Afterward the Naval Wing was raised in July 1952, thus completing the true representation of all services in the Corps.</p>
    
        <h3 class="title-uppercase">Aims of NCC</h3>
        <p>1. To Develop Character, Comradeship, Discipline, Leadership, Secular Outlook, Spirit of Adventure, and Ideals of Selfless Service amongst the Youth of the Country.</p>

         <p>2. To Create a Human Resource of Organized, Trained and Motivated Youth, To Provide Leadership in all Walks of life and be Always Available for the Service of the Nation.</p>

          <p>3. To Provide a Suitable Environment to Motivate the Youth to Take Up a Career in the Armed Forces.</p>

        
       
